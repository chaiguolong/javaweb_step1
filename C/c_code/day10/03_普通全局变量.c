#include <stdio.h>

//1.在{}外面(函数外面)定义的变量叫全局变量
//2.只要定义了全局变量,任何地方都能使用此变量
//3.如果使用变量时,在前面找不到此变量的定义时,需要声明才能使用
//4.全局变量没初始化,默认值为0;
//5.声明只是针对全局变量,局部变量无效
//6.全局变量只能定义一次,可以声明多次
//7.全局变量在编译阶段已将分配空间(函数执行前),只有在整个程序结束,才能自动释放


void fun2(){
	extern int a;

	printf("fun2 a = %d\n", a);
}

int a = 10;

void fun(){

	a = 11;

}


int main(int argc, char *argv[])
{
	fun();

	printf("a = %d\n", a);
	
	fun2();

	return 0;
}
